# import math Module
import  math

# take an integer input from user
num=int(input("Enter a Number: "))

# use sqrt function to ind Square root and Display in Preferred Format
print(f"Square Root of {num} is {math.sqrt(num)}")
# use log function with base e value to find natural Log
print(f"Natural log of {num} is {math.log(num,math.e)}")
# use sin function to calculate the sine value of the user input number
print(f"Sine value of {num} is {math.sin(num)}")